// Template Literals

// O recurso de template literals permite concatenar uma string de forma mais simples;
// Podemos colocar as variáveis entre o texto
// A string fica entre: `Texto`, E as variáveis são colocadas desta maneira: ${variavel}

const userName = 'Alexandre';
const age = 30;

console.log(`O nome do usuario é ${userName} e ele tem ${age} anos,`) // Template Literals
console.log("O nome do usuario é " + userName + " e ele tem " + age + " anos,") // JS NORMAL